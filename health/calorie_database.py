def get_calorie_info(food_name):
    """Get calorie information for a food"""
    food_name_lower = food_name.lower().strip()
    
    # Direct match
    if food_name_lower in CALORIE_DATABASE:
        return CALORIE_DATABASE[food_name_lower]
    
    # Partial match - check if food name contains any database key
    for key, calories in CALORIE_DATABASE.items():
        if key in food_name_lower or food_name_lower in key:
            return calories
    
    # Default value for unknown foods
    return 100

def get_all_calorie_foods():
    """Get all foods with their calorie information"""
    return sorted(list(CALORIE_DATABASE.items()))

def search_calorie_foods(query):
    """Search for foods matching the query"""
    query_lower = query.lower().strip()
    results = []
    
    for food_name, calories in CALORIE_DATABASE.items():
        if query_lower in food_name:
            results.append({
                'name': food_name,
                'calories': calories,
                'unit': '100g' if calories > 50 else 'serving'
            })
    
    return results
